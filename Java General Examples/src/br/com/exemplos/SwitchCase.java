package br.com.exemplos;

public class SwitchCase {
	
	public static void main(String[] args){
		
		int dia =3;
		//Exact Operations
		switch(dia){
		case 1:
			System.out.println("sunday");
			break;
		case 2:
			System.out.println("monday");
			break;
		case 3:
			System.out.println("tuesday");
			break;
			
		default : System.out.println("Outro dia");
			break;
		}
	}

}
